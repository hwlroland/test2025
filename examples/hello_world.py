# 这是一个简单的Python程序示例

# 打印欢迎信息
print('='*50)
print('欢迎使用我的第一个Python程序！')
print('='*50)

# 获取用户输入
用户名 = input('请输入你的名字：')

# 使用条件语句
if 用户名:
    print(f'你好，{用户名}！很高兴认识你！')
else:
    print('你没有输入名字，但我还是很高兴认识你！')

# 创建一个简单的循环
print('\n让我们来数一数：')
for i in range(1, 6):
    print(f'第 {i} 个数')

print('\n程序结束，感谢使用！')
